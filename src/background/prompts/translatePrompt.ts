// 中文翻译 Prompt 构建模块

/**
 * 构建中文翻译为英文的 prompt（要求 native 表达）
 */
export function buildTranslatePrompt(chineseText: string): string {
  return `请将以下中文翻译成最地道的英文表达，并提供详细的翻译说明和替代方案：

"${chineseText}"

请按照以下格式，使用 HTML 标签组织内容，用中文回答：

1. **最佳翻译**（最地道的表达）：
   [你选择的翻译，这必须是最 native、最地道的英文表达]

2. **翻译理由**：
   - 为什么选择这个翻译：[详细解释为什么这个翻译最地道，包括语言习惯、文化背景、语境适配等]
   - 语言特点：[说明这个表达体现了哪些 native speaker 的语言特点]
   - 适用场景：[说明这个翻译在什么场景下最合适]

3. **其他可选表达**（按地道程度排序）：
   - 选项 1：[替代表达 1] - [简要说明适用场景或细微差别]
   - 选项 2：[替代表达 2] - [简要说明适用场景或细微差别]
   - 选项 3：[替代表达 3] - [简要说明适用场景或细微差别]
   （提供 3-5 个替代表达，并说明每个的适用场景和与最佳翻译的差别）

4. **使用建议**：
   - 正式场合：[适合正式场合的表达]
   - 日常交流：[适合日常交流的表达]
   - 书面语：[适合书面语的表达]
   - 口语：[适合口语的表达]

请使用以下 HTML 标签：
- <h3> 用于小标题
- <p> 用于段落
- <strong> 用于强调
- <ul> 和 <li> 用于列表
- <code> 用于英文原文
- <em> 用于例句或说明

确保：
1. 最佳翻译必须是最 native、最地道的表达
2. 翻译理由要详细且有说服力
3. 替代表达要真实可用，并说明使用场景
4. 所有内容都要用中文解释，但英文翻译要地道`;

}

