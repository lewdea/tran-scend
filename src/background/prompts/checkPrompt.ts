// 英文检查 Prompt 构建模块

/**
 * 构建英文检查的 prompt（检查是否清晰、是否符合 native 表达）
 */
export function buildCheckPrompt(englishText: string): string {
  return `请检查以下英文表达，评估其清晰度和是否符合 native speaker 的表达习惯：

"${englishText}"

请按照以下格式，使用 HTML 标签组织内容，用中文回答：

1. **整体评估**：
   - 清晰度：[评估这段英文是否清晰易懂，是否有歧义]
   - 地道程度：[评估是否符合 native speaker 的表达习惯，是否自然流畅]
   - 总体评分：[优秀/良好/需要改进/有明显问题]

2. **问题分析**（如果存在问题）：
   - 语法问题：[如有语法错误，指出并解释]
   - 表达问题：[如有不地道的表达，指出并解释为什么不够地道]
   - 清晰度问题：[如有不清晰或容易产生歧义的地方，指出并解释]
   - 语境适配：[评估是否适合当前语境，是否有更好的表达方式]

3. **改进建议**（按地道程度排序，最地道的放在最前面）：
   - 建议 1（最推荐）：[最地道的表达方式] - [详细说明为什么这个表达最地道，包括语言习惯、文化背景、语境适配等]
   - 建议 2：[替代表达 1] - [说明适用场景和与建议 1 的差别]
   - 建议 3：[替代表达 2] - [说明适用场景和与建议 1 的差别]
   （如果原文已经很地道，可以只提供 1-2 个微调建议；如果问题较多，提供 3-5 个改进建议）

4. **使用建议**：
   - 当前表达适合的场景：[说明当前表达在什么场景下可以使用]
   - 最佳表达适合的场景：[说明最推荐的表达在什么场景下最合适]
   - 其他建议适合的场景：[说明其他建议的表达分别适合什么场景]

请使用以下 HTML 标签：
- <h3> 用于小标题
- <p> 用于段落
- <strong> 用于强调
- <ul> 和 <li> 用于列表
- <code> 用于英文原文
- <em> 用于例句或说明

确保：
1. 如果原文已经很地道，要明确指出并给予肯定
2. 如果存在问题，要详细说明问题所在
3. 改进建议必须是最地道的 native 表达
4. 所有建议都要说明为什么地道，以及适用场景
5. 所有内容都要用中文解释，但英文表达要地道`;

}

