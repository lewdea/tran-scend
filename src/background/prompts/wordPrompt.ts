// 单词学习 Prompt 构建模块

/**
 * 构建单词学习的 prompt
 */
export function buildLearnWordPrompt(word: string): string {
  return `请详细解释这个英文单词："${word}"

请按照以下格式，使用 HTML 标签组织内容，用中文回答：
1. **发音音标**（区分美式和英式）：
   - 美式：[美式音标]  - 英式：[英式音标]

2. **中文含义和词性**：
   - [词性1]：[中文含义1]
   - [词性2]：[中文含义2]
   （列出所有常见含义和对应的词性）

3. **词源背景和同类单词**：
   解释这个词的核心概念、词源背景，以及语义相关的同类单词。例如：drop 强调"向下掉"的动作，所以可以是"掉落"、"水滴"等。

4. **Be Native**（Native Oral English）：
   - [搭配1]：[中文解释] - [例句]
   - [搭配2]：[中文解释] - [例句]
   （提供 native speaker 常用的口语案例和实际使用场景）

请使用以下 HTML 标签：
- <h3> 用于小标题
- <p> 用于段落
- <strong> 用于强调
- <ul> 和 <li> 用于列表
- <code> 用于音标和单词
- <em> 用于例句

确保内容详细、准确、实用。`;
}

