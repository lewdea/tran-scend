// 短句学习 Prompt 构建模块

/**
 * 构建短句翻译+讲解的 prompt
 */
export function buildLearnPhrasePrompt(phrase: string): string {
  return `请翻译这个英文短句或短语，并提供简要讲解："${phrase}"

请按照以下格式，使用 HTML 标签组织内容，用中文回答：

1. **翻译**：
   [准确的中文翻译]

2. **讲解**：
   - 语法要点：[简要说明语法结构或特殊用法]
   - 词汇解析：[解释关键词汇的含义和用法]
   - 使用场景：[说明这个表达在什么情况下使用]
   - 相关表达：[提供类似的表达方式或同义替换]

请使用以下 HTML 标签：
- <h3> 用于小标题
- <p> 用于段落
- <strong> 用于强调
- <ul> 和 <li> 用于列表
- <code> 用于英文原文
- <em> 用于例句

确保翻译准确，讲解简洁实用。`;
}

